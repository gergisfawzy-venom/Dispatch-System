function makeShift(
  id: number,
  guardId: number,
  siteId: number,
  dayOffset: number,
  startTime: string,
  endTime: string,
  status: Shift["status"],
  role = "Guard"
): Shift {
  const date = isoDay(addDays(monday, dayOffset));
  const isStarted = status === "active" || status === "completed" || status === "late" || status === "missed";
  
  return {
    id,
    guardId,
    siteId,
    date,
    startTime,
    endTime,
    actualClockIn: isStarted ? startTime : undefined,
    actualClockOut: status === "completed" ? endTime : undefined,
    role,
    status,
    confirmedAt:
      status === "confirmed" || status === "active" || status === "completed"
        ? new Date()
        : undefined,
    tasks: [
      { id: id * 100 + 1, text: "Check south gate", completed: status === "completed" },
      {
        id: id * 100 + 2,
        text: "Verify perimeter",
        completed: status === "completed" || status === "active",
      },
      {
        id: id * 100 + 3,
        text: "Log visitor entries",
        completed: status === "completed",
      },
      {
        id: id * 100 + 4,
        text: "Radio check each hour",
        completed: false,
      },
    ],
  };
}

